package ddz

type CardSlice []uint8

func CardRank(card uint8) uint8 {
    return card & 0x0F
}

func CardSuit(card uint8) uint8 {
    return card & 0xF0
}

func Card(suit uint8, rank uint8) uint8 {
    return suit | rank
}

